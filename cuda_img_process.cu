int t_difference(struct timespec *start, struct timespec *end, long long int *difference) 
{
  long long int ds =  end->tv_sec - start->tv_sec;
  long long int dn =  end->tv_nsec - start->tv_nsec;

  if(dn < 0 ) 
  {
    ds--;
    dn += 1000000000;
  }
  *difference = ds * 1000000000 + dn;
  return !(*difference > 0);
}

